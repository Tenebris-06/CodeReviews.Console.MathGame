public class Problem
{
    public string Text;
    public string Answer;

    public void setText(string Text)
    {
        this.Text = Text;
    }

    public void setAnswer(string Answer)
    {
        this.Answer = Answer;
    }

    public string getText()
    {
        return Text;
    }
    
    public string getAnswer()
    {
        return Answer;
    }
}